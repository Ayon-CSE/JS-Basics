// 3 types of execution contexts in JavaScript are

// 1. global execution Context(this) - This is the default context that is created when the JavaScript engine starts executing code.It represents the global scope and contains all the global variables and functions.

// 2. Function execution context - This context is created whenever a function is invoked. It contains information about the function being executed, its parameters, local variables, and the value of 'this'.

// 3. Eval execution context - This context is created when the eval() function is called. It allows the execution of code represented as a string.