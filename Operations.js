// ******* Opeations *******
console.log(2 ** 3); // 8, exponentiation operator, 2 raised to the power of 3

let s = "a";
let s2 = "b";

console.log(s + s2); // "ab", string concatenation

console.log("1" + 2); //12, "1" is converted to string and concatenated with 2
console.log(1 + "2"); //12, 1 is converted to string and concatenated with "2"
console.log("1" + 2 + 3); // 123, "1" + 2 is "12", then "12" + 3 is "123"
console.log(1 + 2 + "3"); // 33, 1 + 2 is 3, then 3 + "3" is "33"

console.log(+true); // 1, true is converted to 1
console.log(+""); // 0, empty string is converted to 0
console.log(+"123"); // 123, string "123" is converted to number 123

